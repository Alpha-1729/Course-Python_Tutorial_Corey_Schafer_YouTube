#!/usr/bin/python3
# Loops And Iterations While And For Loops

"""
>>>> Break and Continue.
        break keyword will completely break out of the loop.
        continue keyword will move on to the next iteration of the loop.

>>>>
>>>>
>>>>
"""

nums = [1, 2, 3, 4, 5]

# Looping through a list.
for num in nums:
    print(num)

# Normal for loop.
for i in range(0, 10):
    print(i)

# While loop.
x = 0
while x < 10:
    print(x)
    x += 1
